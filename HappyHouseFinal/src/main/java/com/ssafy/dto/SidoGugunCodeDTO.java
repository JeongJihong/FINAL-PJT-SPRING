package com.ssafy.dto;

public class SidoGugunCodeDTO {

	private String sidoCodeData;
	private String sidoNameData;
	private String gugunCodeData;
	private String gugunNameData;

	public String getSidoCode() {
		return sidoCodeData;
	}

	public void setSidoCode(String sidoCode) {
		this.sidoCodeData = sidoCode;
	}

	public String getSidoName() {
		return sidoNameData;
	}

	public void setSidoName(String sidoName) {
		this.sidoNameData = sidoName;
	}

	public String getGugunCode() {
		return gugunCodeData;
	}

	public void setGugunCode(String gugunCode) {
		this.gugunCodeData = gugunCode;
	}

	public String getGugunName() {
		return gugunNameData;
	}

	public void setGugunName(String gugunName) {
		this.gugunNameData = gugunName;
	}

}
